<span class=title><i>ffiinniiss</i></span><span class=subtitle>(2012)</span> for chamber orchestra
<p>
Written for the Seattle Rock Orchestra, <i>ffiinniiss</i> derives its inspiration from classic rock. My favorite part of
these songs are the final notes, where whammy bars detune strings and drummers let loose to explore new timbres. <i>ffiinniiss</i> highlights the textures and tonalities of these epic rock endings with loose yet aggressive sounds for the orchestra.
