<span class=title><i>paint music for three parts</i></span><span class=subtitle>(2005)</span> for any ensemble (3+)
<p>

In <i>paint music for three parts</i>, the events were determined by the splatters made by five flicks of a paint brush.
After transcribing the paint into graphic notation, each gesture was shaped and developed timbrally along a spectrum of pitched and percussive sound.
