<span class=title><i>murmur</i></span><span class=subtitle>(2012)</span> for chamber orchestra
<p>
<i>murmur</i> explores through sound the subtleties of human consciousness, awareness and experience. As our society becomes more interconnected, it can feel as if time is accelerating and the sense of self, of experience and intention, can be lost in a scattered existence. By slowing the experience of time through music, I hope to bring the listener's attention to detail. This focus can open a door to a deeper awareness of the environment, sensations in the body, and other people.
