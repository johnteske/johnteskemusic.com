<span class=title><i>biguine</i></span><span class=subtitle>(2011)</span> for Bb clarinet and double bass
<p>

Biguine is a very rhythmic style of music that originated in Martinique. To me, this is a lost music&mdash;most modern versions may contain the essential musical components (a main melody, call and response, and improvised solos), but lack the original energy that makes this style so distinctive. In this piece, the bass and clarinet emerge from an ambiguous texture and combine to create the biguine style for just a moment before fading away. Commissioned by David Seerveld.
