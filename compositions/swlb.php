<p>
<span class=title><i>Space Weather Listening Booth</i></span><span class=subtitle>(2013)</span> with Nat Evans
<p>

<i>Space Weather Listening Booth</i> is an immersive acoustic and electronic performance piece based on the aurora borealis, by Seattle composers Nat Evans and John Teske. Listeners hear the collision of the different space weather events that cause the aurora borealis, realized through an electronic track in surround sound and live performers encircling the audience. Premiered as a sound installation with miniature private performances at Seattle's ONN/OF Festival, Space Weather Listening Booth has since been adapted for live performance.
<br />
<br />
Teske and Evans used geomagnetic data, information about solar wind and other phenomenon, and interpreted this data through a series of sounds that interact and slowly change over time. Additionally, to represent the auroral band that rotates around the poles of the earth, the composers plotted a course for the sound to migrate and turn slowly around the listeners. Combined with live performers, <i>Space Weather Listening Booth</i> is a unique sound experience that allows one to hear and feel the movement of these great forces, and experience time and physical space through a new lens.
<br />
<br />
The data used in this piece was collected at magnetic observatories. We thank the national institutes that support them and INTERMAGNET for promoting high standards of magnetic observatory practice (www.intermagnet.org).
