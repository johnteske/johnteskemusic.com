<br />for violoncello and double bass
<p>

<i>Ocean Love Scene in Two Keys</i> was written for cellist Amelia Bowler during the Marrowstone Music Festival.
The music paints a story of a boy that falls in love with a girl&mdash;but the girl loves the ocean. Heartbroken, the boy drowns himself in the ocean.
