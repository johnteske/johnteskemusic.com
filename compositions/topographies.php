<span class=title><i>topographies</i></span><span class=subtitle>(2014)</span> for any ensemble
<p>
<i>topographies</i> is a series of graphic scores which requires performers to navigate through contour maps composed of musical symbols. The pieces are a study on individual and group intention. Each player creates a narrative as they traverse the score, sometimes carving their own path and at other times traveling to reunite with the ensemble. <i>topographies</i> was premiered by Neil Welch and Ivan Arteaga, saxophones; Natalie Mai Hall, violoncello; John Teske, double bass; Greg Campbell, percussion.
