<p>
<span class=title><i>rain</i></span><span class=subtitle>(2005)</span> for violin, viola, violoncello, and double bass
<p>

The inspiration for <i>rain</i> came from a landscape on the Olympic peninsula near Kingston, WA.
Dark clouds hung over a valley, filtering the light into a deep blue and a layer of mist hung above the frost-covered soil. Despite the static environment, there was a potential for growth.
It is from this place that <i>rain</i> begins.
